package By_me;

	public class Example4 {
		
		public static void main(String[] args) {
					
					for(int i=1; i<=5; i++)     //row
					{
						for(int j=1; j<i; j++)    //space
						{
							System.out.print(" ");
						}
						for(int k=i; k<=5; k++)    //column
						{
							System.out.print("*");
						}
						System.out.println(" ");
					}
				}

			}




		
		



